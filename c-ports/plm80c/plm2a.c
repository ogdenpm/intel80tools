bool EnterBlk()
{
    if (blkSP < 0x14) {
        blkSP = blkSP + 1;  
        return true;
    } else {
        if (blkOverCnt == 0) {
          Tx2SyntaxError(ERR204);   /*  LIMIT EXCEEDED: NUMBER OF ACTIVE */
                        /*  PROCEDURES and do CASE GROUPS */
          EmitTopItem();
        }
        blkOverCnt = blkOverCnt + 1;  
        return false;
    }
}



bool ExitBlk()
{
    if (blkOverCnt > 0) { 
        blkOverCnt = blkOverCnt - 1;  
        return false;
    } else if (blkSP > 0) { 
        blkSP = blkSP - 1;  
        return true;
    }
    else
    {
        Tx2SyntaxError(ERR205); /*  ILLEGAL NESTING OF BLOCKS, ENDS not BALANCED */
        EmitTopItem();
        return false;
    }
}


