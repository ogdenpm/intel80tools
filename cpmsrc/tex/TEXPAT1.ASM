	;TEX PATCH #1: 27 JUN 78

	;THIS PATCH CORRECTS AN ERROR WHICH OCCURS DURING 
	;".NA" MODE WHERE A WORD IS DUPLICATED WHEN THE 
	;OUTPUT LINE IS EQUAL TO THE LINE LENGTH.

	;PATCH FOLLOWS:

PATCH	EQU	1D43H
L606	EQU	0DABH
TLINE	EQU	1DE5H
WLINE	EQU	1DE6H
ELINE	EQU	1DE8H
NLINE	EQU	1DEAH

ORG	L606

	JMP	PATCH

RETPAT:

ORG	PATCH

	STA	TLINE
	LDA	WLINE
	LXI	H,ELINE
	CMP	M
	JP	RETPAT
	LDA	NLINE
	STA	WLINE
	JMP	RETPAT
END
